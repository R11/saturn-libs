const uint8_t* saturn_font_glyph(int ch)
{
    if (ch < SATURN_FONT_FIRST_CHAR || ch > SATURN_FONT_LAST_CHAR) return NULL;
    return &saturn_font_8x8[(ch - SATURN_FONT_FIRST_CHAR) * SATURN_FONT_CHAR_H];
}

void saturn_font_row_to_4bpp(uint8_t src, uint8_t fg, uint8_t out[4])
{
    int i;
    /* Two pixels per output byte; high nibble = leftmost pixel of the
     * pair. fg is 1..15; bg is always 0 (transparent). */
    for (i = 0; i < 4; ++i) {
        uint8_t hi = (src & (uint8_t)(0x80u >> (i * 2))) ? fg : 0;
        uint8_t lo = (src & (uint8_t)(0x80u >> (i * 2 + 1))) ? fg : 0;
        out[i] = (uint8_t)((hi << 4) | lo);
    }
}

void saturn_font_to_4bpp(uint8_t fg, uint8_t* dst)
{
    int g, row;
    if (!dst) return;
    for (g = 0; g < SATURN_FONT_CHAR_COUNT; ++g) {
        for (row = 0; row < SATURN_FONT_CHAR_H; ++row) {
            uint8_t src = saturn_font_8x8[g * SATURN_FONT_CHAR_H + row];
            saturn_font_row_to_4bpp(src, fg, &dst[g * 32 + row * 4]);
        }
    }
}
